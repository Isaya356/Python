# Основні функції модуля math:
# Корені та степені:

# math.sqrt(x) — квадратний корінь з x.
# math.pow(x, y) — підносить x до степеня y.
# Логарифми:

# math.log(x) — натуральний логарифм числа x (по основі e).
# math.log10(x) — логарифм по основі 10.
# math.log2(x) — логарифм по основі 2.
# Тригонометричні функції:

# math.sin(x) — синус кута (у радіанах).
# math.cos(x) — косинус кута (у радіанах).
# math.tan(x) — тангенс кута (у радіанах).
# Для обернених функцій:
# math.asin(x) — арксинус.
# math.acos(x) — арккосинус.
# math.atan(x) — арктангенс.
# Константи:

# math.pi — число π (приблизно 3.14159).
# math.e — число Ейлера e (приблизно 2.71828).
# Гіперболічні функції:

# math.sinh(x) — гіперболічний синус.
# math.cosh(x) — гіперболічний косинус.
# math.tanh(x) — гіперболічний тангенс.
# Інші корисні функції:

# math.factorial(x) — факторіал числа x.
# math.gcd(x, y) — найбільший спільний дільник для x і y.
# math.degrees(x) — переводить радіани в градуси.
# math.radians(x) — переводить градуси в радіани.



